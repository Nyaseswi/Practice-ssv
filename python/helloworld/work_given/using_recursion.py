def sum_of_natural_numbers(n):
    if n == 1:
        return 1
    else:
        return n + sum_of_natural_numbers(n-1)

# Example usage
n = 7
result = sum_of_natural_numbers(n)
print(f"The sum of the first {n} natural numbers is: {result}")

